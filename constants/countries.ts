/**
 * Encontra o pa√≠s pelo c√≥digo de discagem no n√∫mero de telefone
 */
export function findCountryByDialCode(phoneNumber: string): Country | undefined {
  const digits = phoneNumber.replace(/\D/g, '');
  
  // Ordena por tamanho do dialCode (maior primeiro) para evitar conflitos
  // Ex: +595 antes de +59
  const sortedCountries = [...COUNTRIES].sort((a, b) => {
    const aDigits = a.dialCode.replace(/\D/g, '');
    const bDigits = b.dialCode.replace(/\D/g, '');
    return bDigits.length - aDigits.length;
  });
  
  return sortedCountries.find(country => {
    const dialDigits = country.dialCode.replace(/\D/g, '');
    return digits.startsWith(dialDigits);
  });
}

